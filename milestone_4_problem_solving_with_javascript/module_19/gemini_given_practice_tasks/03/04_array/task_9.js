/**
 * একটি সংখ্যার অ্যারে নিন (যেমন: [10, 20, 30, 40, 50])। সাধারণ for লুপ ব্যবহার করে অ্যারের সবগুলোর সংখ্যার যোগফল বের করুন এবং লুপের বাইরে এসে যোগফলটি প্রিন্ট করুন।
 */

const numbers = [10, 20, 30, 40, 50];
let sum = 0;

for (let i = 0; i < numbers.length; i++) {
  sum += numbers[i];
}
console.log(sum);
